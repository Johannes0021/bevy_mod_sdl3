pub fn mouse_button_from_sdl(mouse_button: SdlMouseButton) -> Option<MouseButton> {
    Some(match mouse_button {
        SdlMouseButton::Left => MouseButton::Left,
        SdlMouseButton::Middle => MouseButton::Middle,
        SdlMouseButton::Right => MouseButton::Right,
        SdlMouseButton::X1 => MouseButton::Back,
        SdlMouseButton::X2 => MouseButton::Forward,
        SdlMouseButton::Unknown => return None,
    })
}

pub fn touch_event_from_sdl(
    phase: TouchPhase,
    finger_id: i64,
    x: f32,
    y: f32,
    pressure: f32,
    window: Entity,
) -> TouchInput {
    let force = if pressure != 0.0 {
        Some(ForceTouch::Normalized(pressure as f64))
    } else {
        None
    };

    TouchInput {
        phase,
        position: Vec2::new(x, y),
        window,
        force,
        id: finger_id as u64,
    }
}
